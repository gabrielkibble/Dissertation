print(f"Loading {SYNTHETIC_INPUT}...")
df_synth = pd.read_csv(SYNTHETIC_INPUT)

# 2. Create Template Libraries
print("Indexing Context Libraries...")

idx_ddos = df_real[df_real["category"] == "DDoS"].index.tolist()
idx_recon = df_real[df_real["category"] == "Reconnaissance"].index.tolist()
# Check for Theft (some versions of the dataset label it differently)
idx_theft = df_real[df_real["category"].str.contains("Theft|Exfiltration", case=False, na=False)].index.tolist()

# FAILSAFES
if len(idx_ddos) == 0: 
    # If DDoS is missing (unlikely), try 'DoS'
    idx_ddos = df_real[df_real["category"] == "DoS"].index.tolist()

if len(idx_recon) == 0:
    print("Warning: Recon templates missing. Fallback to DDoS.")
    idx_recon = idx_ddos
    
if len(idx_theft) == 0:
    print("Warning: Theft templates missing. Fallback to Recon.")
    idx_theft = idx_recon

print(f"  - DDoS Templates: {len(idx_ddos)}")
print(f"  - Theft Templates: {len(idx_theft)}")
print(f"  - Recon Templates: {len(idx_recon)}")

# 3. Smart Imputation
print("Synthesizing Final Dataset...")
final_rows = []

bot_ips = ["192.168.100.147", "192.168.100.148", "192.168.100.149", "192.168.100.150"]
victim_ip = "192.168.100.3"

num_rows = len(df_synth)
current_ids = np.arange(9000000, 9000000 + num_rows)
current_seqs = np.arange(100000, 100000 + num_rows)
