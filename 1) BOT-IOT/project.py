class PacketCVAE(nn.Module):
    def __init__(self, input_dim, hidden_dim, latent_dim, num_classes):
        super(PacketCVAE, self).__init__()
        self.input_dim = input_dim
        
        # Embed Class ID -> Vector of size 5
        self.embed = nn.Embedding(num_classes, 5)
        
        # --- ENCODER ---
        # Input = Features + Class_Embedding (5)
        self.enc_input_size = input_dim + 5
        self.enc1 = nn.Linear(self.enc_input_size, hidden_dim)
        self.enc2 = nn.Linear(hidden_dim, hidden_dim // 2)
        
        self.fc_mu = nn.Linear(hidden_dim // 2, latent_dim)
        self.fc_logvar = nn.Linear(hidden_dim // 2, latent_dim)
        
        # --- DECODER ---
        # Input = Latent(z) + Class_Embedding (5)
        self.dec_input_size = latent_dim + 5
        self.dec1 = nn.Linear(self.dec_input_size, hidden_dim // 2)
        self.dec2 = nn.Linear(hidden_dim // 2, hidden_dim)
        self.dec_out = nn.Linear(hidden_dim, input_dim)  # Output is just features
        
    def encode(self, x, c):
        # x is (Batch, Features)
        # c is (Batch) -> embed -> (Batch, 5)
        c_emb = self.embed(c)
        in_vec = torch.cat([x, c_emb], dim=1)
        
        h = F.relu(self.enc1(in_vec))
        h = F.relu(self.enc2(h))
        return self.fc_mu(h), self.fc_logvar(h)
    
    def reparameterize(self, mu, logvar):
        std = torch.exp(0.5 * logvar)
        eps = torch.randn_like(std)
        return mu + eps * std
    
    def decode(self, z, c):
        c_emb = self.embed(c)
        in_vec = torch.cat([z, c_emb], dim=1)
        
        h = F.relu(self.dec1(in_vec))
        h = F.relu(self.dec2(h))
        return self.dec_out(h) # Returns (Batch, Features)
        
    def forward(self, x, c):
        mu, logvar = self.encode(x, c)
        z = self.reparameterize(mu, logvar)
        recon_x = self.decode(z, c)
        return recon_x, mu, logvar

