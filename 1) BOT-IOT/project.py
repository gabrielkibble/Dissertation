    if row["bytes"] > HIGH_BYTE_THRESHOLD:
        chosen_idx = np.random.choice(idx_theft)
    elif row["rate"] > HIGH_RATE_THRESHOLD:
        chosen_idx = np.random.choice(idx_ddos)
    else:
        chosen_idx = np.random.choice(idx_recon)
    
    # Copy Costume (Real Context)
    template_row = df_real.loc[chosen_idx].to_dict()
    
    # B. Overwrite Body (Synthetic Features)
    for feat in GENERATED_FEATURES:
        template_row[feat] = row[feat]
        
    # C. Update Metadata
    template_row["pkSeqID"] = current_ids[i]
    template_row["seq"] = current_seqs[i]
    template_row["stime"] = timestamps[i]
    template_row["ltime"] = timestamps[i] + row["dur"]
    template_row["saddr"] = np.random.choice(bot_ips)
    template_row["daddr"] = victim_ip
    template_row["sport"] = np.random.randint(1024, 65535)
    template_row["dport"] = 80
    template_row["attack"] = 1
    
    final_rows.append(template_row)

