# selecting the only 4 tactics that inherently generate statistically distinct network signatures

# the dataset authors did not simulate complex lateral movement or credential access scenarios, talk about the limitation
# but overall this is still the best dataset - listed reasons in my google doc

def get_majority_label(cid, df_labeled):
    if cid == -1: return "Noise" # -1 is noise cluster in DBSCAN
    
    # Get all rows belonging to this cluster
    cluster_data = df_labeled[df_labeled['cluster'] == cid]

    # Find the most frequent category (mode)
    majority_category = cluster_data['category'].mode()[0]
    
    # Map the dataset category to a MITRE ATT&CK Tactic
    mapping = {
        "Theft":          "Exfiltration",
        "Reconnaissance": "Recon (Slow)",
        "DoS":            "Impact (DoS)",
        "DDoS":           "Impact (DoS)",
        "Normal":         "Benign"
    }
    
    return mapping.get(majority_category, "Unknown")

# Dictionary of cluster IDs to MITRE mappings
