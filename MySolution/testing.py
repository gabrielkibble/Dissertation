# 1. The Helper Function for Temperature
def get_scaled_transition_matrix(trans_mat, T=1.0):
    """
    Returns a new transition matrix with temperature scaling applied.
    T < 1.0: Sharpening (Makes likely transitions even MORE likely).
    T > 1.0: Flattening (Increases randomness/exploration).
    """
    if T == 1.0:
        return trans_mat.copy()
    
    # Use power method to scale probabilities while preserving 0.0 constraints
    # (We don't want to create transitions that are impossible)
    scaled = np.power(trans_mat, 1.0 / T)
    
    # Re-normalize rows so they sum to 1.0
    row_sums = scaled.sum(axis=1, keepdims=True)
    # Avoid division by zero if a row is all zeros (dead end)
    row_sums[row_sums == 0] = 1.0
    
    return scaled / row_sums


def generate_and_save_traffic(model, target_states, samples_per_state=10, temperature=1.0, seq_len=20, output_file="synthetic_traffic.csv"):
    """
    1. Iterates through the list of 'target_states'.
    2. Generates 'samples_per_state' sequences for each.
    3. Reconstructs packet features (Total Len, Dload, etc.) using the State Stats.
    4. Saves to CSV.
    """
    all_data = []
    feature_names = list(model.state_feature_stats[model.states[0]]['mean'].shape) # infer dimension
    # Ideally, we want the original column names. 
    # If you passed feature_cols to fit(), we assume the order is preserved.
    # We will use generic names if we can't find them, but usually 
    # you know what FEATURES you passed in.

    # --- STEP A: TEMPORARY MODEL MODIFICATION ---
    # We back up the original matrix so we don't break the model permanently
    original_transmat = model.transmat_.copy()
    
    if temperature != 1.0:
        print(f"🔥 Applying Temperature T={temperature} (Scaling probabilities)...")
        model.transmat_ = get_scaled_transition_matrix(original_transmat, T=temperature)
    
    print(f"\nStarting Generation Pipeline (T={temperature})...")
    print(f"Targeting {len(target_states)} specific composite types.")
    print(f"Generating {samples_per_state} sequences per type.")

    try:
        for start_node in target_states:
            if start_node not in model.state_to_idx:
                print(f"WARNING: Skipping {start_node} (Not found in model)")
                continue
                
            print(f"  -> Generating for {start_node}...")
            
            for i in range(samples_per_state):
                # 1. Generate State Sequence
                try:
                    seq_states = model.generate_sequence(start_state=start_node, length=seq_len)
                except Exception as e:
                    print(f"     Error generating from {start_node}: {e}")
                    continue

                # 2. Reconstruct Features (The "Cheat" Method)
                for t, state in enumerate(seq_states):
                    stats = model.state_feature_stats.get(state)
                    
                    if stats:
                        # Sample from Gaussian (Mean + Covariance)
                        # This creates the specific packet characteristics for this Cluster+Label
                        feats = np.random.multivariate_normal(stats['mean'], stats['cov'])
                        feats = np.maximum(feats, 0) # Physics check (no negative bytes)
                        
                        # Create Row
                        row = {
                            'sequence_id': f"{start_node}_{i}_T{temperature}", # Unique ID for this flow
                            'step': t,
                            'composite_state': state,
                            'label': state.split('_')[-1], # Extract "Theft" from "17_Theft"
                            # We map features to columns 0, 1, 2... 
                            # Update these keys to match your FEATURES list!
                            'feat_0': feats[0], 
                            'feat_1': feats[1],
                            'feat_2': feats[2]
                        }
                        all_data.append(row)

    finally:
        # --- STEP C: RESTORE MODEL ---
        # Always restore original matrix, even if code crashes
        if temperature != 1.0:
            model.transmat_ = original_transmat
            print("❄️ Restored original transition probabilities.")

    # 3. Save to File
    if all_data:
        df_gen = pd.DataFrame(all_data)
        
        # Rename generic feature columns if we know the inputs
        # CHANGE THIS LIST to match your actual FEATURES variable
        feature_map = {
            'feat_0': 'total_len', 
            'feat_1': 'proto_number', 
            'feat_2': 'dload'
        }
        df_gen.rename(columns=feature_map, inplace=True)
        
        df_gen.to_csv(output_file, index=False)
        print(f"\nSUCCESS: Generated {len(df_gen)} packets.")
        print(f"Saved to: {output_file}")
        return df_gen
    else:
        print("\nFAILED: No data generated.")
        return None
    

