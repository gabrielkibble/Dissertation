# Manual mappings based on the MITRE ATT%CK framework
# I am using the tactics only, not the specific techniques within those tactics, as that would require deep packet inspection
# which requires knowing the content of the payload, I only have the statistics in the csv

# Restriciting the scope to 4, not all 14 as network flow data is blind to the other 10.
